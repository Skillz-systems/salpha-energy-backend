interface ProcessingSession {
  id: string;
  fileInfo: {
    name: string;
    size: number;
    type: string;
  };
  sheets: SheetData[];
  stats: CsvUploadStatsDto;
  batches: Array<{
    sheetName: string;
    batchIndex: number;
    data: any[];
  }>;
  generatedDefaults: any;
}

